class MarchingCubes {
    constructor(size, threshold) {
		//  格子を size x size x size の立方体で組み立てる
		this.size = size;
		//  1次元で用意するが、実際には3次元配列 [k][j][i] として扱う 
		this.grid = new Float32Array(this.size * this.size * this.size);
		
		//  この値を1・0の境界条件とする     
		this.threshold = threshold;
		
		// 作業用 頂点と法線、6面対の各辺ごとに最大1つ必要なので12 x 3（x,y,z）個となる
		this.vertices = new Float32Array(12 * 3);
		this.normals = new Float32Array(12 * 3);

		//	＊　以下の4つのプロパティ名は決まっているので変更不可
		//  三角形1枚を表示するためのものなので 3つの頂点（x,y,z）と、頂点ごとの法線（x,y,z）をそれぞれ配列で用意
		
		//	hasPositionsをtrueにすることで、このpositionArray配列がGLに送られる
		this.hasPositions = true;
		this.positionArray = new Float32Array( 3 * 3 );

		//	hasNormalsをtrueにすることで、このnormalArray配列がGLに送られる
 		this.hasNormals = true;
 		this.normalArray = new Float32Array( 3 * 3 );
	}

    //  baseインディックスから z次元にi[2]  y次元にi[1] x次元にi[0]ずれたインディックスを返す
    //      注意）gridは1次元だが、これをsizeで折り返し [k][j][i]　の3次元で扱うためのメソッド
	index(base, i) {
	    return base + (i[2] * this.size * this.size) + (i[1] * this.size) + i[0];
    }
    
    //  インディックス指定接続点の法線
	normal(i) {
		let x = this.grid[this.index(i, [-1, 0, 0])] - this.grid[this.index(i, [1, 0, 0])];
		let y = this.grid[this.index(i, [0, -1, 0])] - this.grid[this.index(i, [0, 1, 0])];
		let z = this.grid[this.index(i, [0, 0, -1])] - this.grid[this.index(i, [0, 0, 1])];
		return [x, y, z];
	}

    //  インディックスを3次元座標に変換
    position(i) {
        let k = Math.floor(i / (this.size * this.size));
        i -= (k * this.size * this.size);
        let j = Math.floor(i / this.size);
        i -= (j * this.size);

		let joints_par_unit = this.size / 2.0;  //  -1 〜 1 空間での1単位につき、いくらの接続点があるか
		let x = (i - joints_par_unit) / joints_par_unit;
		let y = (j - joints_par_unit) / joints_par_unit;
		let z = (k - joints_par_unit) / joints_par_unit;
		return [x, y, z];
    }
    
	//	i, j, k:格子空間のインディックス grid[k][j][i]
	renderPolygon(i, j, k, renderer) {
        let targetIndex = this.index(0, [i, j, k]);

		//	targetIndexを基準とした、自身を含む相対8頂点の状態を閾値で2値化して、256通りのパターンを決定
        let relation = [        
             [0 ,0, 0],        //     		3 ----- 2   
        	 [1 ,0, 0],        //     	  / |     / |
        	 [1 ,1, 0],        //     	7 ----- 6   |
        	 [0 ,1, 0],        //      |   |   |   |
        	 [0 ,0, 1],        //     	|	0 --|-- 1
        	 [1 ,0, 1],        //      | /     | /
        	 [1 ,1, 1],        //     	4 ----- 5	
        	 [0 ,1, 1],    
        ];
		let edgeTable_index = 0;
        relation.forEach((value, i) => {
	        if (this.grid[this.index(targetIndex, value)] < this.threshold) {
	            edgeTable_index |= (1 << i);
            }
        });
		// 三角形パッチが存在するか？
		let dots = edgeTable[edgeTable_index];
		if (dots === 0) return;

		//  立方体各辺の情報：頂点#1（relationのインディックス）, 頂点#2
		let edge_info = [      
		    [0, 1],         //     		3 --[2]-→ 2
		    [1, 2],         //     	    ↑         ↑
		    [3, 2],         //     	   [3]       [1]
		    [0, 3],         //          |         |
                            //     	 	0 --[0]-→ 1
		    [4, 5],         //    7 --[6]-→ 6
		    [5, 6],         //    ↑         ↑
		    [7, 6],         //   [7]       [5]
		    [4, 7],         //    |         |
		                    //    4 --[4]-→ 5
		    [0, 4],         //      	3       2    
		    [1, 5],         //        [11]    [10]
		    [2, 6],         //       /      /  
		    [3, 7]          //     7       6   
                            //     	     0       1
                            //        [8]     [9]
                            //       /      /  
                            //     4      5			                
		];

        //  線形補完
        function interpolation(a, b, t) {
        	return a + ( b - a ) * t;
        };

        edge_info.forEach((info, edge_index) => {
    		if (dots & (1 << edge_index)) {  //  このエッジに三角形用の頂点（dot）は存在するか？
                //  エッジの両端ポイント番号 （0 - 7：relationのインディックス）
                let no_1 = info[0]; 
                let no_2 = info[1];
                
                //  エッジの両端ポイントのgridインディックス
                let i1 = this.index(targetIndex, relation[no_1]);
                let i2 = this.index(targetIndex, relation[no_2]);
                
                //  エッジを構成する2頂点の法線を計算
            	let n1 = this.normal(i1);    			
            	let n2 = this.normal(i2);

                //  エッジを構成する2頂点の座標を計算                
                let p1 = this.position(i1);
                let p2 = this.position(i2);

                //  頂点・法線補完用の勾配を計算
                let v1 = this.grid[i1];
                let v2 = this.grid[i2];
                let gradient = (this.threshold - v1) / (v2 - v1);

                //  頂点及び法線設定
                let i = edge_index * 3;
        		for (let o = 0; o < 3; o++) {   //  x, y, z 3次元分
            		this.vertices[i + o] = interpolation(p1[o], p2[o], gradient);
        		    this.normals[i + o] = interpolation(n1[o], n2[o], gradient);
                }
    		}
    	});
    	
		//	triTable（三角形頂点インディックス）は1次元配列だが、実際は (3 * 5 + 1) * 256 と考える
		let ti = edgeTable_index * 16;  
		while (triTable[ti] != -1) {	// インディックス -1 が見つかるまでは三角形が存在する					
			let a = 0;	//	positionArray, normalArrayのインディックス
			for (let v = 0; v < 3; v++) {	//	3頂点分
				let b = triTable[ti] * 3;
				for (let xyz = 0; xyz < 3; xyz++) {	//	x, y, z座標分
					this.positionArray[a] = this.vertices[b];
					this.normalArray[a] = this.normals[b];
					a++;
					b++;
				}
				ti++;	//	次の頂点情報
			}				
 			this.count = 3;			//	呼び出したrendererでクリアされるので毎回設定
			renderer( this );	//	3角形描画
		}
	}

	//	マーチングキューブスでボリュームをポリゴン化
	render(renderer) {
		let start = 1;                      //  法線計算時に -1 した接続点の値を使うので
		let end = (this.size - 1) - 1 - 1;  //  法線計算時に +1 した接続点の値を使うので（立方体なので、自接続点に +1 も考慮する）
		for (let k = start; k <= end; k++) {
			for (let j = start; j <= end; j++) {
				for (let i = start; i <= end; i++) {
					this.renderPolygon(i, j, k, renderer);
				}
			}
		}
	}
	
	start() {
    }
	update() {
    }
}

